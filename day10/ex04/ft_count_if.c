int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ret;

	ret = 0;
	i = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			ret++;
		i++;
	}
	return (ret);
}

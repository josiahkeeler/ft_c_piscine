int	ft_strcmp(char *s1, char *s2)
{ 
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return(*s1 - *s2);
}

#include <stdio.h>

int	main(void)
{
	char s1[] = "Hell0";
	char s2[] = "Hell";
	printf("%d", ft_strcmp(s1, s2));
	return (0);
}
